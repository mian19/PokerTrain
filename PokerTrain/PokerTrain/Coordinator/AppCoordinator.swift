import Foundation
import UIKit

class AppCoordinator: CoordinatorProtocol {
    var navigationController: UINavigationController
    
    init(navigationController: UINavigationController) {
        self.navigationController = navigationController
    }
    
    func start() {
          let vc = Onboarding.init(transitionStyle: .scroll, navigationOrientation: .horizontal)
            vc.coordinator = self
            navigationController.pushViewController(vc, animated: true)
    }
    
    func toMain() {
     
        guard let window = UIApplication.shared.windows.first(where: { $0.isKeyWindow }) else {
            return
        }

        // Set the new view controller as the root view controller
        window.rootViewController = MenuTabBarController()
    }
    
    func toLearn() {
        let vc = LearnViewController()
         vc.coordinator = self
         navigationController.pushViewController(vc, animated: true)
    }
    
    func toTimer() {
        let vc = TimerViewController()
         vc.coordinator = self
         navigationController.pushViewController(vc, animated: true)
    }
    
    func toChance() {
        let vc = ChanceViewController()
         vc.coordinator = self
         navigationController.pushViewController(vc, animated: true)
    }
    
    func toCards(selected: Int) -> CardsViewController {
        let vc = CardsViewController()
         vc.coordinator = self
        vc.usedCard = selected
        
         navigationController.pushViewController(vc, animated: true)
        vc.setViews()
        return vc
    }
    
    func toReader(topic: Topic) {
        let vc = ReaderViewController()
         vc.coordinator = self
        vc.titleText = topic.rawValue
        vc.article = topic.description
         navigationController.pushViewController(vc, animated: true)
    }
    
    func toQuiz(type: QuizType) {
        let vc = QuizViewController()
         vc.coordinator = self
        vc.questions = type.QuizPack
         navigationController.pushViewController(vc, animated: true)
    }
    
    func toSettings() {
        let vc = SettingsViewController()
         vc.coordinator = self
         navigationController.pushViewController(vc, animated: true)
    }
    
    func toBack() {
        self.navigationController.popViewController(animated: true)
    }
}

