            Покеристы мечтают собрать флеш-рояль — самую сильную комбинацию в техасском холдеме. Однако, получить оплату в таких ситуациях сложно. Проще выиграть стек оппонента, собрав нечитаемые стрит на T8 или натсовый флеш по А5.

            Когда блайнды не так велики по отношению к стеку, игрокам рекомендуется разыгрывать больше рук. Однако, «раздувать» банки стоит только в ситуациях, когда у вас уже есть натс или на руках монстр-дро с большим количеством аутов на усиление.

            Средние стеки
            Спустя несколько уровней в турнире стеки начинают уменьшаться. Поддерживая стартовое количество фишек, в блайндах стек станет в несколько раз меньше. Здесь действовать нужно уже аккуратнее. От розыгрыша маргинальных и пограничных рук лучше отказаться вовсе.

            Линия поведения здесь будет максимально похожа на тайтово-агрессивную игру, когда покерист ждет сильную стартовую комбинацию и разыгрывает ее максимально агрессивно, чтобы удвоиться.

            Короткие стеки
            Здесь ситуация становится одновременно и проще, и сложнее. Проще, поскольку розыгрыш будет редко доходить до постфлопа. Уже на префлопе приходится принимать решение: сбрасывать карты в пас или ставить олл-ин. В этом и заключается сложность, что игроки принимают решение сразу на все фишки.

            Хочется дождаться карманных тузов, чтобы выставиться наверняка старше. Но ждать такое количество раздач — непозволительная роскошь. Поэтому профессионалы изучают игру в пуш-фолдных стеках, чтобы принимать правильные по математике решения.

            Стратегия игры в кэш
            Игра в кэш (на наличные деньги) отличается от МТТ, поскольку здесь блайнды не растут, а значит не нужно беспокоиться, что через час 10,000 фишек превратятся в 20 ББ из 100ББ.

            Однако, здесь есть несколько стратегий, которые зависят от размеров стека, с которым покерист осознанно садится за игровой стол.

            BSS. Стратегия больших стеков, когда за стол участник садится со 100 ББ или больше.
            MSS. Стратегия средних стеков. Здесь число фишек в стеке ограничено от 35 до 50 ББ.
            SSS. Стратегия коротких стеков. Рабочий стек всего от 15 до 25 ББ.
            Каждый делает сам свой выбор, с каким стеком садиться за стол. Однако, большинство покер-румов вводят ограничение по минимальному размеру стека для поддержания интереса игры у любителей.

            Турнирная стратегия
            Эффективная тактика в МТТ серьезно отличается от кэша, поскольку на разных стадиях турнира величина стека изменяется. Начинается турнир в глубоких стеках, потом переходит в стадию средних и нередко доходит до пуш-фолдных.

            На принятие решений влияют несколько сторонних факторов:

            Призовая зона. Все хотят попасть в деньги, поэтому перед началом призовой зоны линии розыгрыша становятся аккуратнее.
            Финальный стол. Чем меньше игроков становится за столом, тем больше замедляется каждая раздача. Участники с коротким стеком стараются максимально продлить свою жизнь в турнире, поскольку разбежка в призовых становится ощутимой.

             
